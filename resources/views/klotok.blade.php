                                    <h6>Kopi Klotok </h6>
                                    </div>  
                                
                                <br>
                                <p>Warung Kopi Klotok di Jalan Kaliurang KM 16, Pakem, Yogyakarta dalam tiga tahun ini seperti menjadi destinasi wajib pelancong ketika jalan-jalan ke Kota Gudeg. Warung jadul bergaya Jawa tersebut booming setelah foto-fotonya ramai diunggah para penikmat kuliner di media sosial berbagi gambar Instagram.

Bangunan joglo khas rumah tradisional Jawa Tengah menjadi daya tarik bagi warung itu. Apalagi, letaknya berada tepat di tepi sawah. Suasana ndeso pun langsung terasa ketika pengunjung datang.</p>
<br>
<p>Tempo menyambangi Kopi Klotok pada Selasa, 24 April lalu. Pagi-pagi benar, pukul 07.00, warung tepi sawah itu sudah buka. Puluhan kendaraan pun sudah terparkir di halaman. Bahkan, bus-bus rombongan juga telah tampak memenuhi jalan masuk warung. 

“Ini masih terhitung sepi. Kalau akhir pekan atau libur panjang, warung pasti lebih ramai berkali-kali lipat,” kata Theresia, 25 tahun, pelanggan Kopi Klotok, saat ditemui di warung tersebut.


Jalan masuk ke warung itu adalah pintu kayu yang rendah. Beberapa pengunjung kudu sedikit menunduk. Dalam filosofi Jawa, hal ini memiliki makna bentuk penghormatan dan sopan-santun tamu terhadap pemilik rumah.</p>
<br>
<img src="image/klotok2.jpg" alt="">
<br>
<br>
<p>Tiba di ruangan pertama, pengunjung akan langsung menemui sebuah ruangan yang lapang. Di sisi kanan terdapat meja panjang dengan beragam lauk. Ada sayur lodeh, sayur lombok ijo, telur, tempe, tahu, dan masakan rumahan khas Jawa Tengah lainnya. Pengunjung bisa langsung memilih.

Kata Etik, salah satu karyawan warung itu, sayur lodeh adalah favorit pelanggan. Kabarnya, sayur lodeh pasti habis menjelang sore. Padahal Kopi Klotok buka sampai malam. “Jadi paling kalau datang malam, hanya kebagian lauk telur tanpa sayur,” tutur Etik. 

Setelah mengambil makan ala prasmanan, pengunjung memilih tempat duduk. Bisa di dalam rumah joglo, di serambi belakang, atau di luar, yakni lesehan di tepi sawah. Orang-orang biasanya berebut tempat di serambi belakang. Sebab, spot tersebut paling strategis. Sembari makan, pengunjung bisa menikmati ke hamparan sawah dan pohon-pohon kelapa. 

Di dalam rumah joglo itu dipajang ornamen pernak-pernik tempo dulu. Ada radio lawas, sepeda onthel, dan lampu teplok. Sedangkan bangku dan meja yang digunakan tergolong perabot lawasan. Model kursi set babon angrem dengan anyaman rotan ialah salah satu contohnya.</p>
                                    
