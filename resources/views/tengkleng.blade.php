                                   <p>Tengkleng Gajah merupakan olahan kambing dalam porsi “gajah” alias potongan tulangnya berukuran besar. Selain tengkleng yang diolah dengan bumbu dan rempah-rempah khusus sehingga menciptakan citarasa yang khas, di warung ini juga menyediakan sate, tongseng, dan nasi goreng kambing.

Apa yang terlintas di benak Anda tatkala mendengar nama Warung Tengkleng Gajah? Apakah bahan utama dari masakan tengkleng adalah daging gajah? Jika Anda penasaran, tak ada salahnya untuk mengunjungi warung yang terletak di Jalan Kaliurang ini dan mencicipi hidangannya yang sudah tersohor.

Tengkleng Gajah bukanlah tengkleng daging gajah, melainkan tengkleng kambing. Istilah tengkleng gajah muncul akibat potongan tulang kambing yang diolah menjadi tengkleng berukuran besar. Begitu juga dengan porsi hidangannya adalah porsi jumbo, alias porsi gajah. Berawal dari hal tersebut maka orang-orang lebih mengenal Warung Sari Roso Mulyo ini sebagai Warung Tengkleng Gajah.

Tengkleng sendiri merupakan masakan sejenis sup khas Solo yang berbahan dasar daging, jeroan, dan tulang kambing. Sepintas menyerupai gulai kambing, hanya saja citarasanya berbeda dengan kuah yang lebih encer. Tengkleng Gajah disajikan dalam kondisi panas, dengan daging yang masih menempel di tulang. Aroma dan tampilannya begitu menggiurkan. Rasanya pun kaya dan menyegarkan.

Pengunjung biasanya akan memakai sedotan untuk menikmati sumsum yang tersembunyi di balik tulang. Soal citarasa, tengkleng gajah dijamin istimewa sebab untuk memasak irisan tulang kambing ini diperlukan waktu sekitar 3 jam. Selain empuk, aneka rempah-rempah yang digunakan sebagai bumbu pun bisa merasuk ke dalam daging.

Selain tengkleng, di warung ini juga tersedia beragam olahan kambing seperti sate kambing, tongseng kambing, juga nasi goreng kambing. Menurut pemiliknya, total ada 11 menu olahan daging kambing. Pengunjung pun bisa memilih menu sesuai selera mereka mau digoreng, dibakar, ditongseng dengan citarasa manis, pedas, bahkan gurih. Warung tengkleng gajah buka setiap hari dari pukul 09.00 – 21.00 WIB. Di musim liburan atau akhir pekan biasanya tengkleng akan cepat habis, karena itu datanglah lebih awal supaya tidak kehabisan.

Lokasi dan Akses
Warung Tengkleng Gajah ini terletak di Dusun Bulurejo, Minomartani, Ngaglik, Sleman. Meskipun terletak di perkampungan, akses menuju warung ini terbilang mudah. Dari pusat Kota Yogyakarta silahkan ikuti Jalan Kaliurang ke utara hingga kilometer 9,3. Sebelum lampu merah Merapi View ada pertigaan, silahkan belok kanan (arah timur). Susurilah jalan tersebut. Lokasi Tengkleng Gajah berjarak sekitar 1 km dari Jalan Kaliurang dan lokasinya di kanan jalan.</p> 
                                   
                                    



                                    
                              
                                    
